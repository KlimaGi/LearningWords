package org.klimagi.learning.words.model;

public class Duo { // dvieju atributu klase
	
	private String lt;
	private String en;
	
	public Duo(String en, String lt) {  
		this.lt = lt;
		this.en = en;
	}
	public String getLt() {
		return lt;
	}
	public void setLt(String lt) {
		this.lt = lt;
	}
	public String getEn() {
		return en;
	}
	public void setEn(String en) {
		this.en = en;
	}	
// kaip padaryti atvirkscia varianta
	
	

}
